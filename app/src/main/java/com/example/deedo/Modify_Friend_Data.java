package com.example.deedo;

public class Modify_Friend_Data {

    private String Modify_Friend_name;
    private String Modify_Friend_id;

    public Modify_Friend_Data(String modify_Friend_name, String modify_Friend_id) {
        Modify_Friend_name = modify_Friend_name;
        Modify_Friend_id = modify_Friend_id;
    }

    public String getModify_Friend_name() {
        return Modify_Friend_name;
    }

    public void setModify_Friend_name(String modify_Friend_name) {
        Modify_Friend_name = modify_Friend_name;
    }

    public String getModify_Friend_id() {
        return Modify_Friend_id;
    }

    public void setModify_Friend_id(String modify_Friend_id) {
        Modify_Friend_id = modify_Friend_id;
    }
}
