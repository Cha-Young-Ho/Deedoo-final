package com.example.deedo.BarChart;

public class BarChart_list_data {
    String tag;
    String second;

    public BarChart_list_data(String tag, String second) {
        this.tag = tag;
        this.second = second;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getSecond() {
        return second;
    }

    public void setSecond(String second) {
        this.second = second;
    }
}
